package cn.rustle;
/**
 * 演示变量
 */
public class Demo02 {
    public static void main(String[] args) {
        /**
         * 变量的声明
         * 1.数据类型 变量名
         * 2.可以在同一行声明同类型的变量，但变量名不允许重复
         * 3.要想使用这个变量，需要提前声明
         */
        //声明整数变量a
        int a;//分号表示一行的结束

        //声明整数变量b
        int b;

        //声明3个整数类型c,d,e
        //将注释写在代码前面，表示忽略这行代码，不进行编译
        int c,d,e;//将注释写在代码后面，表示对代码的说明
    }
}
